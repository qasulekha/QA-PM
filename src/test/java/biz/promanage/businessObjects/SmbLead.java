package biz.promanage.businessObjects;

public class SmbLead {
    private  String name;
    private  String mobileNo;
    private  String email;
    private  String businessName;
    private  String businessCategory;
    private  String cityName;
    private  String companyWebsiteUrl;
    private  String businessProof;
    private  boolean googleProfile;
    private  String noOfEmployees;
    private  String annualRevenue;
    private  String googleBusinessProfileCount;
    private  String googleProfileActive;

    public SmbLead() {
    }

    public String getName() {
        return name;
    }

    public String getMobileNo() {
        return mobileNo;
    }

    public String getEmail() {
        return email;
    }

    public String getBusinessName() {
        return businessName;
    }

    public String getBusinessCategory() {
        return businessCategory;
    }
    public String getCityName() {
        return cityName;
    }

    public String getCompanyWebsiteUrl() {
        return companyWebsiteUrl;
    }

    public String getBusinessProof() {
        return businessProof;
    }

    public boolean getGoogleProfile() {
        return googleProfile;
    }

    public String getNoOfEmployees() {
        return noOfEmployees;
    }

    public String getAnnualRevenue() {
        return annualRevenue;
    }

    public String getGoogleBusinessProfileCount() {
        return googleBusinessProfileCount;
    }

    public String getGoogleProfileActive() {
        return googleProfileActive;
    }
}
